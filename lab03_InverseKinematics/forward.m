function [x,y]=forward(t1,t2,x0,y0,l1,l2)
  % Модель прямої кінематики робота
  % Вхідні параметри:
  % t1,t2 - кути відхилення сегментів (градуси)
  % x0,y0 - положення місця кріплення основи робота
  % l1,l2 - довжини сегментів
  % Вихідні параметри:
  % x,y - очікуване положення робочого сегменту робота
  x=x0+[0, l1*cosd(t1) , l1*cosd(t1)+l2*cosd(t1+t2)];
  y=y0+[0, l1*sind(t1) , l1*sind(t1)+l2*sind(t1+t2)];
endfunction
